/* ===========================================================================
   build-manifest.mjs
   סורק את img/, מנרמל שמות קבצים (עברית עם ניקוד) ל-id ascii יציב,
   מעתיק תמונות ל-public/images/<categoryId>/<id>.webp,
   וכותב src/data/vocabulary.generated.ts.

   הרצה:  node scripts/build-manifest.mjs   (או: npm run manifest)
   =========================================================================== */

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const IMG_DIR = path.join(ROOT, 'img');
const OUT_IMG_DIR = path.join(ROOT, 'public', 'images');
const OUT_TS = path.join(ROOT, 'src', 'data', 'vocabulary.generated.ts');

/** הסרת ניקוד וטעמים (U+0591–U+05C7) כדי להשוות שמות תיקיות */
function stripNikud(s) {
  return s.normalize('NFC').replace(/[֑-ׇ]/g, '');
}

/** מיפוי שם-תיקייה (ללא ניקוד) -> מזהה קטגוריה */
const FOLDER_TO_CATEGORY = {
  'דמויות': 'characters',
  'חיות': 'animals',
  'ירקות': 'vegetables',
  'מוזיקה': 'music',
  'פרות': 'fruits',
  'צבעים': 'colors',
  'קנוחים': 'desserts',
};

/** תת-קבוצות בקטגוריית חיות (שם תיקייה ללא ניקוד -> מזהה) */
const SUBGROUP_TO_ID = {
  'חיות בר': 'wild',
  'חיות חווה': 'farm',
  'חיות ים': 'sea',
  'ציפורים': 'birds',
};

/** תעתיק עברי->לטיני בסיסי לשמות קבצים קריאים */
const TRANSLIT = {
  'א': 'a', 'ב': 'b', 'ג': 'g', 'ד': 'd', 'ה': 'h', 'ו': 'o', 'ז': 'z',
  'ח': 'ch', 'ט': 't', 'י': 'y', 'כ': 'k', 'ך': 'k', 'ל': 'l', 'מ': 'm',
  'ם': 'm', 'נ': 'n', 'ן': 'n', 'ס': 's', 'ע': 'a', 'פ': 'p', 'ף': 'f',
  'צ': 'tz', 'ץ': 'tz', 'ק': 'k', 'ר': 'r', 'ש': 'sh', 'ת': 't',
};

function slugify(heWithNikud) {
  let s = stripNikud(heWithNikud);
  // צירופים עם גרש: ג'->j, צ'->ch, ז'->zh
  s = s.replace(/ג['’׳]/g, 'j').replace(/צ['’׳]/g, 'ch').replace(/ז['’׳]/g, 'zh');
  let out = '';
  for (const ch of s) {
    if (ch === ' ') out += '_';
    else if (TRANSLIT[ch] !== undefined) out += TRANSLIT[ch];
    else if (/[a-z0-9_]/i.test(ch)) out += ch.toLowerCase();
    // שאר התווים מושמטים
  }
  return out.replace(/_+/g, '_').replace(/^_|_$/g, '') || 'item';
}

async function walkWebp(dir) {
  const out = [];
  const entries = await fs.readdir(dir, { withFileTypes: true });
  for (const e of entries) {
    const full = path.join(dir, e.name);
    if (e.isDirectory()) out.push(...(await walkWebp(full)));
    else if (e.isFile() && e.name.toLowerCase().endsWith('.webp')) out.push(full);
  }
  return out;
}

async function main() {
  const files = (await walkWebp(IMG_DIR)).sort();
  const items = [];
  const usedIds = new Set();

  // נקה ובנה את תיקיית היעד מחדש
  await fs.rm(OUT_IMG_DIR, { recursive: true, force: true });

  for (const file of files) {
    const rel = path.relative(IMG_DIR, file);
    const parts = rel.split(path.sep);
    const catFolder = stripNikud(parts[0]);
    const categoryId = FOLDER_TO_CATEGORY[catFolder];
    if (!categoryId) {
      console.warn(`⚠️  מדלג על תיקייה לא ממופה: ${parts[0]}`);
      continue;
    }
    if (categoryId === 'colors') continue; // צבעים מצוירים, לא תמונות

    let subgroup;
    if (parts.length === 3) {
      subgroup = SUBGROUP_TO_ID[stripNikud(parts[1])];
    }

    const he = path.basename(file, '.webp').normalize('NFC');
    let base = slugify(he);
    let id = `${categoryId}_${base}`;
    let n = 2;
    while (usedIds.has(id)) id = `${categoryId}_${base}_${n++}`;
    usedIds.add(id);

    const destDir = path.join(OUT_IMG_DIR, categoryId);
    await fs.mkdir(destDir, { recursive: true });
    await fs.copyFile(file, path.join(destDir, `${id}.webp`));

    items.push({
      id,
      he,
      categoryId,
      ...(subgroup ? { subgroup } : {}),
      image: `/images/${categoryId}/${id}.webp`,
    });
  }

  const header = `/* AUTO-GENERATED by scripts/build-manifest.mjs — אל תערוך ידנית.
   הרץ: npm run manifest */
import type { VocabItem } from './types';

export const VOCABULARY: VocabItem[] = ${JSON.stringify(items, null, 2)};
`;

  await fs.mkdir(path.dirname(OUT_TS), { recursive: true });
  await fs.writeFile(OUT_TS, header, 'utf8');

  const byCat = items.reduce((m, it) => ((m[it.categoryId] = (m[it.categoryId] || 0) + 1), m), {});
  console.log(`✅ נוצרו ${items.length} פריטים:`, byCat);
  console.log(`   -> ${path.relative(ROOT, OUT_TS)}`);
  console.log(`   -> ${path.relative(ROOT, OUT_IMG_DIR)}/`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
